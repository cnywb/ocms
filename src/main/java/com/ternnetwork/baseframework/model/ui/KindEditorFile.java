package com.ternnetwork.baseframework.model.ui;



public class KindEditorFile {
	
	private Boolean is_dir;
	
	private Boolean has_file;
	
	private Long filesize;
	
	private Boolean is_photo;
	
	private String filetype;
	
	private String filename;
	
	private String datetime;

	public Boolean getIs_dir() {
		return is_dir;
	}

	public void setIs_dir(Boolean is_dir) {
		this.is_dir = is_dir;
	}

	public Boolean getHas_file() {
		return has_file;
	}

	public void setHas_file(Boolean has_file) {
		this.has_file = has_file;
	}

	

	public Long getFilesize() {
		return filesize;
	}

	public void setFilesize(Long filesize) {
		this.filesize = filesize;
	}

	public Boolean getIs_photo() {
		return is_photo;
	}

	public void setIs_photo(Boolean is_photo) {
		this.is_photo = is_photo;
	}

	public String getFiletype() {
		return filetype;
	}

	public void setFiletype(String filetype) {
		this.filetype = filetype;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public String getDatetime() {
		return datetime;
	}

	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}
	
	

}
